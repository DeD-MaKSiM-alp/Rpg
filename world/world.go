package world

// chunkSize — размер одного чанка в клетках.
// Например, при значении 16 один чанк содержит 16x16 тайлов.
// Дальше весь мир будет состоять из таких одинаковых блоков.
const chunkSize = 16

// World хранит бесконечный мир на уровне логики.
// Мир состоит из чанков фиксированного размера, которые создаются лениво
// по мере того, как игрок приближается к соответствующим областям.
type World struct {
	// seed — числовое зерно мира.
	// Оно влияет на процедурную генерацию:
	// один и тот же seed даёт один и тот же мир,
	// а другой seed — уже другой вариант мира.
	seed int

	// chunks — все чанки мира, доступные по их координатам.
	// Ключом является ChunkCoord, значением — указатель на Chunk.
	chunks map[ChunkCoord]*Chunk

	collectedPickups map[PickupKey]bool
}

type PickupKey struct {
	X int
	Y int
}

// ChunkCoord — координаты чанка в мире.
// Это не координаты клетки, а именно номер чанка:
//
//	(0, 0), (1, 0), (-1, 2) и т.д.
type ChunkCoord struct {
	X int
	Y int
}

// Chunk — отдельный кусок мира фиксированного размера.
// Он хранит:
//   - свои координаты в сетке чанков;
//   - локальные тайлы внутри себя.
//
// Важно:
// chunkX/chunkY — это не координаты клетки игрока,
// а позиция чанка среди других чанков мира.
type Chunk struct {
	chunkX  int
	chunkY  int
	tiles   [][]TileType
	pickups []Pickup
}

// NewWorld создаёт новый бесконечный мир с указанным seed
// и инициализирует структуры для чанков и собранных предметов.
// Возвращается указатель, так как мир изменяется по ходу игры.
func NewWorld(seed int) *World {
	return &World{
		seed:             seed,
		chunks:           make(map[ChunkCoord]*Chunk),
		collectedPickups: make(map[PickupKey]bool),
	}
}

// Seed возвращает зерно мира.
// Это удобно для отладки и отображения информации на экране.
func (w *World) Seed() int {
	return w.seed
}
