package world

// Pickup — простой подбираемый объект мира.
//
// На первом этапе он очень простой:
//   - имеет фиксированную позицию в мировых координатах;
//   - может быть собран игроком;
//   - после сбора перестаёт рисоваться и учитываться в логике.
type Pickup struct {
	X         int
	Y         int
	Collected bool
}
